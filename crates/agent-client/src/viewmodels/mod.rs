//! ViewModel 层
//!
//! 负责将业务数据转换为 UI 友好的格式，实现 UI 与业务逻辑的解耦。
//!
//! ## 架构设计
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────┐
//! │                   UI 层 (View)                          │
//! │  components/*.rs - 只负责渲染和用户交互                  │
//! └────────────────────┬────────────────────────────────────┘
//!                      │ 单向数据流
//!                      ↓
//! ┌─────────────────────────────────────────────────────────┐
//! │                ViewModel 层                              │
//! │  viewmodels/*.rs - 数据转换 + 状态管理                   │
//! └────────────────────┬────────────────────────────────────┘
//!                      │
//!                      ↓
//! ┌─────────────────────────────────────────────────────────┐
//! │                  业务逻辑层 (Model)                       │
//! │  core/*.rs - 纯业务逻辑                                   │
//! └─────────────────────────────────────────────────────────┘
//! ```

pub mod dependency;

pub use dependency::{
    DependencyAction, DependencyViewModel, DependencyViewModelState, UIDependencyItem,
    UIDependencyStatus,
};
